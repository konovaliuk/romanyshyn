package com.training.entities;

public class State {
	
	private String name;
	private Integer id;
	
	public State() {
		
	}
	
	public String getName() {
		return this.name;
	}
	
	public Integer getId() {
		return this.id;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public void setId(int id) {
		this.id = id;
	}
}
